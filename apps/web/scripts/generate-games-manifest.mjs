import { access, mkdir, readdir, writeFile } from "node:fs/promises";
import path from "node:path";

const appRoot = process.cwd();
const gamesDir = path.join(appRoot, "public", "games");
const outputDir = path.join(appRoot, "src", "data");
const outputFile = path.join(outputDir, "games.generated.ts");

function toTitle(slug) {
  return slug
    .split("-")
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
    .join(" ");
}

async function exists(filePath) {
  try {
    await access(filePath);
    return true;
  } catch {
    return false;
  }
}

const entries = await readdir(gamesDir, { withFileTypes: true });

const games = [];

for (const entry of entries) {
  if (!entry.isDirectory()) continue;

  const slug = entry.name;
  const gamePath = path.join(gamesDir, slug);
  const indexPath = path.join(gamePath, "index.html");

  if (!(await exists(indexPath))) continue;

  let thumbnail = "/images/game-placeholder.png";

  for (const fileName of ["thumb.webp", "thumb.png", "thumb.jpg", "thumbnail.webp", "thumbnail.png"]) {
    if (await exists(path.join(gamePath, fileName))) {
      thumbnail = `/games/${slug}/${fileName}`;
      break;
    }
  }

  games.push({
    slug,
    title: toTitle(slug),
    href: `/games/${slug}/index.html`,
    thumbnail,
    category: "Arcade",
    isPublished: true,
  });
}

games.sort((a, b) => a.title.localeCompare(b.title));

await mkdir(outputDir, { recursive: true });

const fileContent = `// AUTO-GENERATED FILE.
// Do not edit manually. Generated by scripts/generate-games-manifest.mjs

export const games = ${JSON.stringify(games, null, 2)} as const;

export type Game = (typeof games)[number];
`;

await writeFile(outputFile, fileContent, "utf8");

console.log(`Generated ${games.length} games at src/data/games.generated.ts`);